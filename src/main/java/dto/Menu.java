package dto;

public class Menu 
{
	private int id;
	private String menu;
	private int price;
	private String img;
	
	public int getId() 
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public String getMenu() 
	{
		return menu;
	}
	public void setMenu(String menu) 
	{
		this.menu = menu;
	}
	public int getPrice()
	{
		return price;
	}
	public void setPrice(int price) 
	{
		this.price = price;
	}
	public String getImg()
	{
		return img;
	}
	public void setImg(String img)
	{
		this.img = img;
	}
		
	
}
